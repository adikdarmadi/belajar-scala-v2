package workingWithDataLiteralsValuesVariablesTypesVariablesTypes

object Literal {

  def main(args: Array[String]) {
    val x: Int = 5
    println(x) //5

    x * 2
    println(x) //5

    x / 5
    println(x) //5

    var a: Double = 2.72
    println(a) //2.72

    a = 355.0 / 113.0
    println(a) //3.1415929203539825

    a = 5
    println(a) //5
  }

}
